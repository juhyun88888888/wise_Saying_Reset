package org.example;

public class WiseSaying {
    int id;
    String content;
    String authorName;
    WiseSaying() {
        this.id = id;
        this.content = content;
        this.authorName = authorName;
    }

    private int getId () {
        return this.id;
    }

    private String getcontent () {
        return this.content;
    }

    private String getAuthorName () {
        return this.authorName;
    }
}


